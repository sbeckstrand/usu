/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x) {
  int low = 0x30;
  int high = 0x39;

  int highDiff = high + (~x + 1);
  int lowDiff = x + (~low + 1);

  return !(lowDiff >> 31) & !(highDiff >> 31);
}
